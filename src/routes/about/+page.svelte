	<p>
		I've always had some level of difficulty expressing my emotions. I remember being struck once
		upon a time, reading that women use much my diverse vocabulary to discuss emotion than men. I
		don't want to necessarily divide folks based on a gender label, although there's a growing body
		of research behind it, but for sure I identified with what I read. I was never 'resentful', I
		was 'angry'. I was never 'fragile', I was 'afraid'. I recognised that although there were these
		words I was using, there was no nuance of meaning or intensity.
	</p>

	<p>
		Towards the end of last year, I came across the excellent watercolour emotion wheel by Abby
		VanMuijen in an <a href="https://pudding.cool/2022/12/emotion-wheel/" target="blank"
			>outstanding interactive art piece on The Pudding</a
		> with Michelle McGhee. If you've never come across it, it's worth going and doing it now. Do it
		in a quiet place, on your own.
	</p>

	<p>
		Abby is an artist, and they took the ideas behind the Geneva Emotion Wheel (I'm assuming), and
		built on it with stronger colour associations. Where the Geneva Emotion Wheel tends to be
		represented on a normal hue wheel, rotating through the colour spectrum from 'R' to 'OYGBIV',
		she has put a lot of thought into colour associations, and built on research done for Pixar's
		film 'Inside Out' (also worth watching if you've never seen it before)
	</p>

	<p>
		I thought about how cool it would be to take advantage of microjournalling to review my
		feelings, and encourage me to develop my own emotional intelligence by making myself more aware
		of how I'm feeling at any given moment, and developing my self-assessment skills for really
		understanding myself better.
	</p>

	<p>
		This tool will allow you to choose one or more emotions from the colour wheel to journal about
		any time you want to. Your emotions will be recorded and you'll be able to see what emotions you
		felt on a particular day, week, month, and over time.
	</p>

	<p>Plans</p>

